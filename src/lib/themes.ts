  altSectionBg: string;     // alternating section (navigation, share)
  // ── Cards ────────────────────────────────────────
  cardBg: string;
  cardBorder: string;
  cardShadow: string;
  cardIconBg: string;       // icon wrapper inside cards
  // ── Typography ───────────────────────────────────
  headingColor: string;
  bodyColor: string;
  mutedColor: string;
  accentColor: string;      // gold equivalent
  accentBg: string;         // light tint of accent
  accentBorder: string;
  // ── Form inputs ──────────────────────────────────
  inputBg: string;
  inputBorder: string;
  inputFocusBorder: string;
  // ── Footer ───────────────────────────────────────
  footerBg: string;
  footerTextMuted: string;
  footerAccent: string;
